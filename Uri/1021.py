value = float(input())

cem = int(value/100)
cinquenta = int((value - (cem * 100))/50)
vinte = int((value - (cem * 100) -(cinquenta * 50))/20)
dez = int((value - (cem * 100) -(cinquenta * 50) -(vinte * 20))/10)
cinco = int((value - (cem * 100) -(cinquenta * 50) -(vinte * 20) -(dez * 10))/5)
dois = int((value - (cem * 100) -(cinquenta * 50) -(vinte * 20) -(dez * 10) -(cinco * 5))/2)
um = int((value - (cem * 100) -(cinquenta * 50) -(vinte * 20) -(dez * 10) -(cinco * 5) -(dois * 2)))

cent = round((float(value) - int(value))*100)
moeda50 = int(cent/50)
moeda25 = int((cent - moeda50*50)/25)
moeda10 = int((cent - moeda50*50 - moeda25*25)/10)
moeda5 = int((cent - moeda50*50 - moeda25*25 - moeda10*10)/5)
moeda1 = (cent - (moeda50*50) - (moeda25*25) - (moeda10*10) - (moeda5*5))

print("NOTAS:")
print("%i nota(s) de R$ 100.00" % cem)
print("%i nota(s) de R$ 50.00" % cinquenta)
print("%i nota(s) de R$ 20.00" % vinte)
print("%i nota(s) de R$ 10.00" % dez)
print("%i nota(s) de R$ 5.00" % cinco)
print("%i nota(s) de R$ 2.00" % dois)
print("MOEDAS:")
print("%i moeda(s) de R$ 1.00" % um)
print("%i moeda(s) de R$ 0.50" % moeda50)
print("%i moeda(s) de R$ 0.25" % moeda25)
print("%i moeda(s) de R$ 0.10" % moeda10)
print("%i moeda(s) de R$ 0.05" % moeda5)
print("%i moeda(s) de R$ 0.01" % moeda1)